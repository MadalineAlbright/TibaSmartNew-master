package com.example.thebeast.afyahelp.MarkerInfo;
public class MarkerInfo {
    private String placeType;
    private String phone_number;
    private String poto_reference;



    public MarkerInfo() {
    }

    public String getPlaceType() {
        return placeType;
    }

    public void setPlaceType(String placeType) {
        this.placeType = placeType;
    }

    public String getPhone_number() {
        return phone_number;
    }

    public void setPhone_number(String phone_number) {
        this.phone_number = phone_number;
    }

    public String getPoto_reference() {
        return poto_reference;
    }

    public void setPoto_reference(String poto_reference) {
        this.poto_reference = poto_reference;
    }
}